class DevicesController < ApplicationController
  before_action :authenticate_user!
  before_action :set_device, only: [:show, :edit, :update, :destroy]

  # GET /devices
  # GET /devices.json
  def index
    @devices = Device.all
  end

  # GET /devices/1
  # GET /devices/1.svg
  def show
    @connected_device = ConnectedDevice.new(device: @device)
    @graph = SingleDeviceGraph.new device: @device#@connected_device
    @png   = @graph.to_png

    respond_to do |format|
      format.html
      format.svg { render plain: @graph.to_svg }
    end
  end

  # GET /devices/new
  def new
    @device = Device.new
    if params[:location]
      @location = Location.friendly.find params[:location]
      @device.location = @location
    end
  end

  # GET /devices/1/edit
  def edit
  end

  # POST /devices
  # POST /devices.json
  def create
    @device = Device.new(device_params)

    if params.dig(:device, :create_new_location).present?
      location = Location.create! name: params.dig(:device, :create_new_location)
      @device.location = location
    end
    if params.dig(:device, :other_kind).present?
      @device.kind = params.dig(:device, :other_kind)
    end

    respond_to do |format|
      if @device.save
        format.html { redirect_to @device, notice: 'Device was successfully created.' }
        format.json { render :show, status: :created, location: @device }
      else
        format.html { render :new }
        format.json { render json: @device.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /devices/1
  # PATCH/PUT /devices/1.json
  def update
    respond_to do |format|
      if @device.update(device_params)
        format.html { redirect_to @device, notice: 'Device was successfully updated.' }
        format.json { render :show, status: :ok, location: @device }
      else
        format.html { render :edit }
        format.json { render json: @device.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /devices/1
  # DELETE /devices/1.json
  def destroy
    @device.destroy
    respond_to do |format|
      format.html { redirect_to devices_url, notice: 'Device was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_device
      @device = Device.friendly.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def device_params
      params.require(:device).permit(:name, :kind, :model, :description, :num_links, :manufacturer, :connector, :url,
                                     :location_id,
                                     images: [])
    end
end
